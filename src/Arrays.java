public class Arrays {
    public static void main(String[] args) {
        int [] m = new int[10];
        m[0] = 1;
        System.out.println(m[0]);
        System.out.println(java.util.Arrays.toString(m));

        int [] m2 = {2,7,3,6,1};
        System.out.println(java.util.Arrays.toString(m2));
    }
}
